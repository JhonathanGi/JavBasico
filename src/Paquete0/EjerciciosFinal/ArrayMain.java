package Paquete0.EjerciciosFinal;


public class ArrayMain {
    
    public static void main(String[] args) {
        
        String animales [] = {
            "Gato",
            "Perro",
            "Pez",
            "Mono",
            "Caballo"
        };

        for (int i = 0; i < animales.length; i++) {
            System.out.println(animales[i]);
        }
    }
}
